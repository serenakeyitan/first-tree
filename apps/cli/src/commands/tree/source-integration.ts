import { existsSync, lstatSync, readFileSync, readlinkSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";

import { SourceBindingMode, TreeMode } from "./binding-state.js";
import {
  BINDING_CONTRACT_MARKER,
  BINDING_MODE_MARKER,
  ENTRYPOINT_MARKER,
  parseGitHubRepoReference,
  parseManagedSourceBindingText,
  SOURCE_INTEGRATION_BEGIN,
  SOURCE_INTEGRATION_END,
  SOURCE_INTEGRATION_FILES,
  SOURCE_INTEGRATION_MARKER,
  SOURCE_STATE_MARKER,
  TREE_MODE_MARKER,
  TREE_REPO_MARKER,
  TREE_REPO_SLUG_MARKER,
  TREE_REPO_URL_MARKER,
  WORKSPACE_ID_MARKER,
} from "./binding-contract.js";
import { ManagedFileAction, upsertWhitepaperFile } from "./skill-lib.js";
import { ensureTrailingNewline } from "./shared.js";

export type SourceIntegrationFile = "AGENTS.md" | "CLAUDE.md";

export type SourceIntegrationUpdate = {
  action: ManagedFileAction;
  file: SourceIntegrationFile;
};

export type SourceIntegrationOptions = {
  bindingMode?: SourceBindingMode;
  entrypoint?: string;
  sourceStatePath?: string;
  treeMode?: TreeMode;
  treeRepoName?: string;
  treeRepoUrl?: string;
  workspaceId?: string;
};

export type GitIgnoreUpdate = {
  action: "created" | "updated" | "unchanged";
  file: ".gitignore";
};

const LOCAL_TREE_TEMP_ROOT = ".first-tree/tmp";

const LOCAL_TREE_GITIGNORE_ENTRIES = [`${LOCAL_TREE_TEMP_ROOT}/`] as const;

export function buildSourceIntegrationBlock(
  treeRepoName: string,
  options?: SourceIntegrationOptions,
): string {
  const details = deriveSourceIntegrationDetails(treeRepoName, options);

  return [
    SOURCE_INTEGRATION_BEGIN,
    "## First Tree integration",
    "",
    "> Managed block — regenerated by `first-tree` tooling. Do not edit by hand.",
    "",
    details.scopeText,
    "",
    "### Binding",
    "",
    ...details.bindingLines,
    "",
    "<!--",
    ...details.metadataLines,
    "-->",
    "",
    "### Before every task",
    "",
    "- Treat this managed block as the canonical local binding contract for which Context Tree this repo belongs to.",
    "- Use the recorded tree repo name, tree repo URL, and GitHub slug from this block as the source of truth when deciding which tree to read or update.",
    "- If you already have that tree repo cloned locally, update it before you read anything else.",
    details.fallbackInstruction,
    ...(details.sourceStatePathValue
      ? [
          `- If \`${details.sourceStatePathValue}\` exists, treat it as legacy migration state only. Do not edit it by hand.`,
        ]
      : []),
    `- Never commit anything under \`${LOCAL_TREE_TEMP_ROOT}/\` to this repo. It is local-only workspace state.`,
    "",
    "### After every task",
    "",
    "- Always ask whether the tree needs updating.",
    "- If the task changed decisions, constraints, rationale, ownership, or shared workspace relationships, open a PR in the tree repo first. Then open the source/workspace code PR.",
    "- If the task changed only implementation details, skip the tree PR and open only the source/workspace code PR.",
    SOURCE_INTEGRATION_END,
  ].join("\n");
}

function deriveSourceIntegrationDetails(
  treeRepoName: string,
  options?: SourceIntegrationOptions,
): {
  bindingLines: string[];
  fallbackInstruction: string;
  metadataLines: string[];
  scopeText: string;
  sourceStatePathValue?: string;
} {
  const bindingMode = options?.bindingMode ?? "standalone-source";
  const treeMode = options?.treeMode ?? "dedicated";
  const entrypoint = options?.entrypoint ?? "/";
  const sourceStatePathValue = options?.sourceStatePath;
  const resolvedTreeRepoName = options?.treeRepoName ?? treeRepoName;
  const treeRepoUrl = options?.treeRepoUrl?.trim() || null;
  const treeRepoSlug = parseGitHubRepoReference(treeRepoUrl ?? undefined) ?? null;
  const workspaceId = options?.workspaceId?.trim() || null;

  return {
    bindingLines: buildBindingLines(
      resolvedTreeRepoName,
      treeRepoSlug,
      bindingMode,
      entrypoint,
      workspaceId,
      treeRepoUrl,
    ),
    fallbackInstruction: buildFallbackInstruction(treeRepoName, treeRepoUrl),
    metadataLines: buildMetadataLines(
      treeRepoName,
      resolvedTreeRepoName,
      treeRepoSlug,
      bindingMode,
      treeMode,
      entrypoint,
      workspaceId,
      treeRepoUrl,
      sourceStatePathValue,
    ),
    scopeText: describeScope(bindingMode, treeMode, treeRepoName, workspaceId),
    sourceStatePathValue,
  };
}

function buildBindingLines(
  resolvedTreeRepoName: string,
  treeRepoSlug: string | null,
  bindingMode: SourceBindingMode,
  entrypoint: string,
  workspaceId: string | null,
  treeRepoUrl: string | null,
): string[] {
  return [
    `- **Tree repo:** \`${resolvedTreeRepoName}\``,
    ...(treeRepoSlug === null ? [] : [`- **GitHub tree repo:** \`${treeRepoSlug}\``]),
    `- **Binding mode:** \`${bindingMode}\``,
    `- **Entrypoint:** \`${entrypoint}\``,
    ...(workspaceId === null ? [] : [`- **Workspace ID:** \`${workspaceId}\``]),
    `- **Tree repo URL:** ${formatTreeRepoUrlDisplay(treeRepoUrl)}`,
  ];
}

function buildMetadataLines(
  treeRepoName: string,
  resolvedTreeRepoName: string,
  treeRepoSlug: string | null,
  bindingMode: SourceBindingMode,
  treeMode: TreeMode,
  entrypoint: string,
  workspaceId: string | null,
  treeRepoUrl: string | null,
  sourceStatePathValue: string | undefined,
): string[] {
  return [
    `${BINDING_CONTRACT_MARKER} managed-block-v1`,
    `${SOURCE_INTEGRATION_MARKER} ${describeBinding(bindingMode, treeMode, treeRepoName)}`,
    `${TREE_REPO_MARKER} \`${resolvedTreeRepoName}\``,
    ...(treeRepoSlug === null ? [] : [`${TREE_REPO_SLUG_MARKER} \`${treeRepoSlug}\``]),
    `${TREE_MODE_MARKER} \`${treeMode}\``,
    `${BINDING_MODE_MARKER} \`${bindingMode}\``,
    `${TREE_REPO_URL_MARKER} ${treeRepoUrl === null ? "pending publish" : `\`${treeRepoUrl}\``}`,
    `${ENTRYPOINT_MARKER} \`${entrypoint}\``,
    ...(workspaceId === null ? [] : [`${WORKSPACE_ID_MARKER} \`${workspaceId}\``]),
    ...(sourceStatePathValue ? [`${SOURCE_STATE_MARKER} \`${sourceStatePathValue}\``] : []),
  ];
}

function formatTreeRepoUrlDisplay(treeRepoUrl: string | null): string {
  return treeRepoUrl === null ? "_pending publish_" : `<${treeRepoUrl}>`;
}

function buildFallbackInstruction(treeRepoName: string, treeRepoUrl: string | null): string {
  if (treeRepoUrl === null) {
    return "- If the tree has not been published yet, work from the local checkout you just bound (or pass `--tree-path` explicitly) until `first-tree tree publish` records the GitHub repo URL.";
  }

  return `- If you do not already have that tree repo cloned locally, clone a temporary working copy from \`${treeRepoUrl}\` into \`${join(LOCAL_TREE_TEMP_ROOT, treeRepoName)}/\`, use it for the current task, and delete it before you finish.`;
}

function describeBinding(
  bindingMode: SourceBindingMode,
  treeMode: TreeMode,
  treeRepoName: string,
): string {
  switch (bindingMode) {
    case "workspace-root":
      return `workspace root bound to shared tree repo \`${treeRepoName}\``;
    case "workspace-member":
      return `workspace member bound to shared tree repo \`${treeRepoName}\``;
    case "shared-source":
      return `source repo bound to shared tree repo \`${treeRepoName}\``;
    default:
      return treeMode === "shared"
        ? `source repo bound to shared tree repo \`${treeRepoName}\``
        : `source repo bound to dedicated tree repo \`${treeRepoName}\``;
  }
}

function describeScope(
  bindingMode: SourceBindingMode,
  treeMode: TreeMode,
  treeRepoName: string,
  workspaceId: string | null,
): string {
  switch (bindingMode) {
    case "workspace-root":
      return `This folder is a workspace root. Install first-tree locally here, keep all Context Tree files only in the shared \`${treeRepoName}\` repo, and keep child repos bound to the same tree${workspaceId === null ? "" : ` for workspace \`${workspaceId}\``}.`;
    case "workspace-member":
      return `This repo is a workspace member. Keep all Context Tree files only in the shared \`${treeRepoName}\` repo and follow the workspace root's binding for shared context updates${workspaceId === null ? "" : ` in workspace \`${workspaceId}\``}.`;
    case "shared-source":
      return `This repo is bound to an existing shared Context Tree. Keep all Context Tree files only in the shared \`${treeRepoName}\` repo.`;
    default:
      return treeMode === "shared"
        ? `This repo is bound to a shared Context Tree. Keep all Context Tree files only in the shared \`${treeRepoName}\` repo.`
        : `This repo is a source/workspace repo. Keep all Context Tree files only in the dedicated \`${treeRepoName}\` repo.`;
  }
}

export function upsertSourceIntegrationFiles(
  root: string,
  treeRepoName: string,
  options?: SourceIntegrationOptions,
): SourceIntegrationUpdate[] {
  return SOURCE_INTEGRATION_FILES.map((file) =>
    upsertSourceIntegrationFile(root, file, treeRepoName, options),
  );
}

function upsertSourceIntegrationFile(
  root: string,
  file: SourceIntegrationFile,
  treeRepoName: string,
  options?: SourceIntegrationOptions,
): SourceIntegrationUpdate {
  const fullPath = join(root, file);
  const exists = existsSync(fullPath);
  const current = exists ? readFileSync(fullPath, "utf-8") : "";
  const normalized = current.replaceAll("\r\n", "\n");
  const nextBlock = buildSourceIntegrationBlock(
    treeRepoName,
    deriveIntegrationOptions(normalized, treeRepoName, options),
  );
  const nextText = replaceManagedSourceIntegrationBlock(normalized, nextBlock);

  if (ensureTrailingNewline(nextText) === ensureTrailingNewline(normalized)) {
    return { action: "unchanged", file };
  }

  writeFileSync(fullPath, ensureTrailingNewline(nextText));
  return { action: exists ? "updated" : "created", file };
}

function deriveIntegrationOptions(
  normalized: string,
  treeRepoName: string,
  options?: SourceIntegrationOptions,
): SourceIntegrationOptions {
  const existing = parseManagedSourceBindingText(normalized);

  return {
    bindingMode: firstDefined(options?.bindingMode, existing?.bindingMode),
    entrypoint: firstDefined(options?.entrypoint, existing?.entrypoint),
    sourceStatePath: firstDefined(options?.sourceStatePath),
    treeMode: firstDefined(options?.treeMode, existing?.treeMode),
    treeRepoName: firstDefined(options?.treeRepoName, existing?.treeRepoName, treeRepoName),
    treeRepoUrl: firstDefined(options?.treeRepoUrl, existing?.treeRepoUrl),
    workspaceId: firstDefined(options?.workspaceId, existing?.workspaceId),
  };
}

function firstDefined<T>(...values: Array<T | null | undefined>): T | undefined {
  for (const value of values) {
    if (value !== undefined && value !== null) {
      return value;
    }
  }
  return undefined;
}

function replaceManagedSourceIntegrationBlock(normalized: string, nextBlock: string): string {
  const managedBlock =
    /<!-- BEGIN FIRST-TREE-SOURCE-INTEGRATION -->[\s\S]*?<!-- END FIRST-TREE-SOURCE-INTEGRATION -->\n?/mu;
  return managedBlock.test(normalized)
    ? normalized.replace(managedBlock, `${nextBlock}\n`)
    : appendManagedBlock(normalized, nextBlock);
}

function appendManagedBlock(current: string, nextBlock: string): string {
  const trimmed = current.trimEnd();
  if (trimmed.length === 0) {
    return `${nextBlock}\n`;
  }
  return `${trimmed}\n\n${nextBlock}\n`;
}

export function upsertLocalTreeGitIgnore(root: string): GitIgnoreUpdate {
  const fullPath = join(root, ".gitignore");
  const exists = existsSync(fullPath);
  const current = exists ? readFileSync(fullPath, "utf-8") : "";
  const lines = current
    .replaceAll("\r\n", "\n")
    .split("\n")
    .filter((line, index, source) => {
      if (index === source.length - 1 && line === "") {
        return false;
      }
      return true;
    });

  let changed = false;

  for (const entry of LOCAL_TREE_GITIGNORE_ENTRIES) {
    if (!lines.includes(entry)) {
      lines.push(entry);
      changed = true;
    }
  }

  if (!changed) {
    return { action: "unchanged", file: ".gitignore" };
  }

  writeFileSync(fullPath, ensureTrailingNewline(lines.join("\n")));
  return { action: exists ? "updated" : "created", file: ".gitignore" };
}

export function ensureWhitepaperSymlink(root: string): ManagedFileAction {
  return upsertWhitepaperFile(root);
}

export function readManagedWhitepaperTarget(root: string): string | null {
  const fullPath = join(root, "WHITEPAPER.md");
  try {
    const stat = lstatSync(fullPath);
    if (!stat.isSymbolicLink()) {
      return null;
    }
    return readlinkSync(fullPath);
  } catch {
    return null;
  }
}

export function removeManagedWhitepaper(root: string): void {
  const fullPath = join(root, "WHITEPAPER.md");
  if (readManagedWhitepaperTarget(root) !== null) {
    rmSync(fullPath, { force: true });
  }
}
